def read_file(filename):
  number_MCS = 3000
  mcs=[]
  param=[]
  with open(filename, "r") as f:
    for line in f:
      if line.startswith("#"):
        continue
      else:
        parts=line.strip().split()
        Step, E = None, None
        Step = int(parts[0])
        E = float(parts[1])
        if Step > number_MCS:
          break
        else:
          mcs.append(Step)
          param.append(E)
  return mcs, param

