# FUNCTION TO AVOID STRAIGHT LINES BECAUSE OF PERIODIC CONDITIONS

def plot_periodic(ax, x, y, z, color, label, L):
    # We add an empty entry to generate the legend
    ax.plot([], [], [], color=color, linewidth=2, label=label)

    for i in range(1, len(x)):
        dx = abs(x[i] - x[i-1])
        dy = abs(y[i] - y[i-1])
        dz = abs(z[i] - z[i-1])

        # If the evolution is small (does not cross the periodic border), we draw the segment
        if dx < L/2 and dy < L/2 and dz < L/2:
            ax.plot(
                [x[i-1], x[i]],
                [y[i-1], y[i]],
                [z[i-1], z[i]],
                color=color,
                linewidth=0.5
            )

