MAX_MCS = 200000

def read_COM_file(filename):
    mcs = []
    x1 = []; y1 = []; z1 = []
    x2 = []; y2 = []; z2 = []
    x3 = []; y3 = []; z3 = []

    with open(filename, "r") as f:
        for line in f:
            if line.startswith("#"):
                continue
            parts = line.strip().split()
            step = int(parts[0])
            if step > MAX_MCS:
                break
            mcs.append(step)
            x1.append(float(parts[1])); y1.append(float(parts[2])); z1.append(float(parts[3]))
            x2.append(float(parts[4])); y2.append(float(parts[5])); z2.append(float(parts[6]))
            x3.append(float(parts[7])); y3.append(float(parts[8])); z3.append(float(parts[9]))

    return np.array(mcs), np.array(x1), np.array(y1), np.array(z1), \
           np.array(x2), np.array(y2), np.array(z2), \
           np.array(x3), np.array(y3), np.array(z3)

def read_diffusion_file(filename):
    mcs = []
    VS1 = []; VS2 = []; VS3 = []; VS_mean = []
    L1 = []; L2 = []; L3 = []; L_mean = []

    with open(filename, "r") as f:
        for line in f:
            if line.startswith("#"):
                continue
            parts = line.strip().split()
            step = int(parts[0])
            if step > MAX_MCS:
                break
            mcs.append(step)
            VS1.append(float(parts[1])); VS2.append(float(parts[2])); VS3.append(float(parts[3]))
            VS_mean.append(float(parts[4]))
            L1.append(float(parts[5])); L2.append(float(parts[6])); L3.append(float(parts[7]))
            L_mean.append(float(parts[8]))

    return np.array(mcs), np.array(VS1), np.array(VS2), np.array(VS3), np.array(VS_mean), \
           np.array(L1), np.array(L2), np.array(L3), np.array(L_mean)

